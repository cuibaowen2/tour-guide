package model;

/**
 * 导游模型
 */
public class TourGuideModel {
    private String id;
    private String tourGuideId;
    // 地理位置--经度
    private String tourGuideLongitude;
    // 地理位置--维度
    private String tourGuideLatitude;
    private String tourGuideNumber;
    private String userId;
    private String tourGuideLabel;
    private String tourGuideSex;
    private String tourGuideAge;
    private String tourGuideName;
    private String tourGuidePhoto;
    private String tourGuideMandarinLevel;
    private String tourGuideIdNumber;
    private String groupVoiceID;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTourGuideId() {
        return tourGuideId;
    }

    public void setTourGuideId(String tourGuideId) {
        this.tourGuideId = tourGuideId;
    }

    public String getTourGuideLongitude() {
        return tourGuideLongitude;
    }

    public void setTourGuideLongitude(String tourGuideLongitude) {
        this.tourGuideLongitude = tourGuideLongitude;
    }

    public String getTourGuideLatitude() {
        return tourGuideLatitude;
    }

    public void setTourGuideLatitude(String tourGuideLatitude) {
        this.tourGuideLatitude = tourGuideLatitude;
    }

    public String getTourGuideNumber() {
        return tourGuideNumber;
    }

    public void setTourGuideNumber(String tourGuideNumber) {
        this.tourGuideNumber = tourGuideNumber;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getTourGuideLabel() {
        return tourGuideLabel;
    }

    public void setTourGuideLabel(String tourGuideLabel) {
        this.tourGuideLabel = tourGuideLabel;
    }

    public String getTourGuideSex() {
        return tourGuideSex;
    }

    public void setTourGuideSex(String tourGuideSex) {
        this.tourGuideSex = tourGuideSex;
    }

    public String getTourGuideAge() {
        return tourGuideAge;
    }

    public void setTourGuideAge(String tourGuideAge) {
        this.tourGuideAge = tourGuideAge;
    }

    public String getTourGuideName() {
        return tourGuideName;
    }

    public void setTourGuideName(String tourGuideName) {
        this.tourGuideName = tourGuideName;
    }

    public String getTourGuidePhoto() {
        return tourGuidePhoto;
    }

    public void setTourGuidePhoto(String tourGuidePhoto) {
        this.tourGuidePhoto = tourGuidePhoto;
    }

    public String getTourGuideMandarinLevel() {
        return tourGuideMandarinLevel;
    }

    public void setTourGuideMandarinLevel(String tourGuideMandarinLevel) {
        this.tourGuideMandarinLevel = tourGuideMandarinLevel;
    }

    public String getTourGuideIdNumber() {
        return tourGuideIdNumber;
    }

    public void setTourGuideIdNumber(String tourGuideIdNumber) {
        this.tourGuideIdNumber = tourGuideIdNumber;
    }

    public String getGroupVoiceID() {
        return groupVoiceID;
    }

    public void setGroupVoiceID(String groupVoiceID) {
        this.groupVoiceID = groupVoiceID;
    }
}
